package JavaEncapsulation;

public class Student {
    
    String name;
    int age;

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public int getAge() {
        return this.age;
    }
    public void setAge(int age) {
        this.age = age;
    }

}
